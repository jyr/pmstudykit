                  <i class="fa fa-text-width"></i>
                  <h3 class="box-title">Integration</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                <li><strong>The project charter</strong> officially sanctions the project. Without a charter, the project cannot begin.</li>
                <p class="text-green">NOTE<br/>
                In matrix organizations, your team doesn’t report to you, so the charter gives you the authority to put them to work.</p>
                <li>The <strong>sponsor</strong> is the person (or people) responsible for paying for the project and is part of all important project decisions.</li>
                <li><strong>Develop Project Charter</strong> is the very first process performed in a project.</li>
                <li>The project charter gives the project manager authority to <strong>do the project work</strong>, and to <strong>assign work</strong> or take control of project resources for the duration of the project. It also gives the project manager authority to <strong>spend money</strong> and use <strong>other company resources</strong>.</li>
                <li><strong>Facilitation techniques</strong> (like brainstorming) are ways to get all of your stakeholders on the same page about your project goals and your approach to meeting them.</li>
                <li>The <strong>business case</strong> tells everyone why the company should do the project. It’s an input to building the project charter that tells everyone that the project actually started, explains what it’s going to deliver, and authorizes the project manager to do the work.</li>
                <p class="text-green">NOTE<br/>
                The project charter shouldn’t be too detailed. You shouldn’t have to update the charter every time you change something about your project for it to stay accurate.</p>

                <li>The project charter does not include details about what will be produced or how. Instead, it contains the <strong>summary milestone schedule</strong>.</li>
                <li>Two additional inputs to Develop Project Charter are the <strong>agreements</strong> and the <strong>statement of work</strong>. The contract is what you agreed to do, although not all projects have a contract. The statement of work lists all of the <strong>deliverables</strong> that you and your team need to produce.</li>
                  <p class="text-green">NOTE<br/>
                  Agreements are sometimes referred to as contracts on the exam.</p>
                <li><strong>Enterprise environmental factors</strong> tell you how your company does business. An important one is the project <strong>management information system</strong>, which determines how work is assigned, and makes sure that tasks are done in the right order.</li>
                <li><strong>Organizational process assets</strong> tell you how your company normally runs projects. One of the most important assets is <strong>lessons learned</strong>, which is where you write down all of the valuable historical information that you learn throughout the project to be used later.</li>                
                <li>Remember that the Project Management plan is formal—which means that it’s <strong>written down</strong> and <strong>distributed</strong> to your team.</li>
                <li>You may get a question on the exam that asks what to do when you encounter a change. You <strong>always</strong> begin dealing with change by <strong>consulting the Project Management plan.</strong></li>
                <li>The <strong>project management information</strong> system is a part of your company’s enterprise environmental factors, and it’s generally part of any change control system. It defines how <strong>work is assigned to people</strong>.</li>
                <li>The Project Management plan includes <strong>baselines</strong>: snapshots of the scope, schedule, and budget that you can use to keep track of them as they change.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

