                  <h3 class="box-title">Risk</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                  <li>The main output of all of the Risk Management planning processes is <strong>updated project documents</strong>, and the main document that gets updated is the <strong>risk register.</strong></li>
                  <li>The first step in Risk Management is <strong>Identify Risks</strong>, where you work with the whole team to figure out what risks could affect your project.</li>
                  <li>Qualitative and quantitative analysis are all about <strong>ranking risks</strong> based on their probability and impact.</li>
                  <li>Qualitative analysis is where you take the <strong>categories</strong> in your risk plan and <strong>assign</strong> them to each of the risks that you’ve identified.</li>
                  <li>Quantitative analysis focuses on <strong>gathering numbers</strong> to help evaluate risks and <strong>making the best decisions</strong> about how to handle them.</li>
                  <li><strong>Decision tree analysis</strong> is one kind of <strong>expected monetary value</strong> analysis. It focuses on adding up all of the costs of decisions being made on a project so that you can see the overall value of risk responses.</li>
                  <li>To calculate EMV, be sure to <strong>treat all negative risks as negative numbers</strong> and all <strong>opportunities as positive ones</strong>. Then add up all of the numbers on your decision tree.</li>
                  <li>Don’t forget <strong>watchlists</strong>. They let you monitor lower-priority risks so that you can see if triggers for those risks occur and you need to treat them as higher priorities.</li>
                  <li>All of the processes in Risk Management are <strong>Planning or Monitoring and Controlling processes</strong>. There are <strong>no Executing</strong> processes here. Since the goal is to plan for risks, there is no need to focus on actually doing the work. By then, it’s too late to plan for risks.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

