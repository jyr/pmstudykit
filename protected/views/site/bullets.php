                  <h3 class="box-title">Quality</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                    <li><strong>Inspection</strong> means <strong>checking each deliverable</strong> for defects. That means checking your specs and your documentation, as well as your product, for bugs.</li>
                    <li>The better you plan the quality activities for your project, the <strong>less inspection</strong> you need.</li>
                    <li>Ishikawa diagrams help you to <strong>pinpoint the causes</strong> of defects.</li>
                    <li>The <strong>rule of seven means</strong> that any time you have seven data points in a row that fall on the same side of the mean on a control chart, you need to figure out why.</li>
                    <li>When data points fall above the upper limit or below the lower limit on a control chart, the process is out of control.</li>
                    <li>For the test, using any of the seven basic quality tools is <strong>usually</strong> a good indication that you are in the Control Quality process.</li>
                    <li><strong>Ishikawa, fishbone, and cause-and-effect</strong> diagrams are all the same thing.</li>
                    <li><strong>Scatter charts</strong> help you look at the <strong>relationship</strong> between two different kinds of data.</li>
                    <li><strong>Flowcharts</strong> help you get a handle on how processes work by showing all of the decision points graphically.</li>
                    <li><strong>Grade</strong> refers to the <strong>value</strong> of a product, but not its quality. So, a product can be low-grade by design, and that’s fine. But if it’s a low-quality product, that’s a big problem.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

