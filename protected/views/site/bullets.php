                  <h3 class="box-title">Organization</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                    <li>Functional managers have all the power in a functional organization. Project managers have the power in a projectized organization.</li>
                    <li>If a question on the exam doesn’t state an organization type, assume it’s referring to a matrix organization. That means the PM is responsible for making budgets, assigning tasks to resources, and resolving conflicts.</li>
                    <li>Project coordinators and expediters don’t exist in a projectized organization.</li>
                    <li>A project expediter keeps track of project status only. A project coordinator has some authority, and usually reports to someone higher up in the company. Neither role has as much power or authority as a real project manager, even though expediters or coordinators may have “Project Manager” printed on their business cards.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

