                  <h3 class="box-title">Cost</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                    <li><strong>Parametric estimation</strong> is used in Estimate Costs and Determine Budget.</li>
                    <li><strong>Cost aggregation</strong> is rolling up costs from the work package level to the control account level so that the numbers can be followed down through the WBS hierarchy.</li>
                    <li><strong>Control accounts</strong> are high-level WBS items that are used to track cost estimates. They do not represent activities or work packages. They represent the cost of the work packages and activities that appear under them in the WBS.</li>
                    <li>The main outputs of Estimate Costs are the activity cost estimate and the basis of estimates. The main outputs of Determine Budget are the cost baseline and project funding requirements.</li>
                    <li>You will get questions on the exam asking you to select between projects using <strong>net present value</strong> (NPV) or <strong>benefit cost ratio</strong> (BCR). Always choose the project with the biggest NPV or BCR!</li>
                    <li><strong>Lifecycle costing</strong> means estimating the money it will take to support your product or service when it has been released.</li>
                    <li><strong>Rough order of magnitude Estimation</strong> is estimating with very little accuracy at the beginning of a project and then refining the estimate over time. It’s got a range of –25% to +75%.</li>
                    <li>A <strong>management reserve</strong> is money set aside to cover unplanned, unexpected costs. Your project’s funding requirements need to cover both the budget in the cost baseline and the management reserve.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

