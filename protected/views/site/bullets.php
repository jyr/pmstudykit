                  <i class="fa fa-text-width"></i>
                  <h3 class="box-title">Human Resources</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                  <li>Project managers use their <strong>general management skills</strong> (“soft skills”) to motivate and lead the team.</li>
                  <li>In a matrix organization, the project manager doesn’t have <strong>legitimate</strong> power, because the team doesn’t directly report to the project manager.</li>
                  <li>The most effective forms of power are <strong>reward power</strong>, where the project manager sets up rewards and recognition for the team, and<strong> expert power</strong>, which means the team respects the project manager’s technical expertise.</li>
                  <li><strong>Referent power</strong> is power that’s based on identifying with or admiring the power holder.</li>
                  <li><strong>Punishment power</strong> is the least effective form of power. The project manager should never punish a team mem“ber in front of peers or managers!</li>
                  <li>Project managers should be familiar with modern <strong>theories of motivation and management</strong>.</li>
                  <li><strong>McGregor’s Theories X and Y </strong>state that there are poor Theory X managers who don’t trust their teams, and good Theory Y managers who do.</li>
                  <li><strong>Maslow’s Hierarchy of Needs</strong> is the theory that says that people can’t achieve “self-actualization” (full potential) or esteem (feeling good and important) until lower needs like safety and security are met.</li>
                  <li><strong>Herzberg’s Theory</strong> says that it’s difficult to motivate people unless hygiene factors like a paycheck and job security are already in place.</li>
                  <li><strong>Expectancy Theory</strong> holds that people only respond to rewards that are tied to goals they feel they have a realistic chance of achieving.</li>
                  <li>Bruce Tuckman’s five stages of team development are forming (the team still finding their roles), <strong>storming</strong> (the team forming opinions), <strong>norming </strong>(adjusting work habits to help the team), <strong>performing</strong> (working like a well-oiled machine), and <strong>adjourning</strong> (closing down the project).</li>
                  <li><strong>Resources, schedules, and priorities</strong> cause 50% of project problems and conflicts. Personality conflicts are actually the least likely cause.</li>
                  <li>The best way to solve a problem is to <strong>confront</strong> it, which means doing your research, figuring out what’s causing the problem, and fixing it.</li>
                  <li><strong>Withdrawal</strong> happens when someone gives up and walks away from the problem, usually because he’s frustrated or disgusted. If you see a team member doing this, it’s a warning sign that something’s wrong.</li>
                  <li>Don’t be fooled by questions that make it sound like “confronting” is a bad thing. <strong>Confronting is just another word for problem solving.</strong></li>
                  <li><strong>Smoothing</strong> is minimizing the problem, and it can help cool people off while you figure out how to solve it.</li>
                  <li>You should <strong>compromise only if you can’t confront the problem.</strong></li>
                  <li><strong>Forcing</strong> means making a decision by simply picking one side. It’s a really ineffective way to solve problems.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

