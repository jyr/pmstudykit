                  <h3 class="box-title">Scope</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                    <li><strong>Product scope</strong> means the features and functions of the product or service being built. <strong>Project scope</strong> means the work that’s needed to build the product.</li>
                    <li><strong>Functional requirements</strong> are the behavior of the product. <strong>Nonfunctional requirements</strong> are implicit expectations about the product.</li>
                    <li><strong>Scope Management</strong> is about figuring out all of the work that’s going to be needed for the project, and making sure only that work is done—and nothing else.</li>
                    <li>The <strong>Scope Management plan</strong> is created as part of the Project Management plan. It defines the process you’ll use for defining scope and managing changes to it.</li>
                    <li>You’ll need to know the <strong>order of processes</strong> for the exam. A good way to remember them is to understand how the output of one process is used as the input for another.</li>
                    <li>The<strong>Create WBS</strong> process is a really important process on the PMP exam.</li>
                    <li>You create the WBS by <strong>decomposing</strong> large work products into work packages.</li>
                    <li>To finalize the WBS, <strong>control accounts</strong> are established for the work packages.</li>
                    <li>The <strong>WBS dictionary</strong> is a description of each work package listed in the WBS.</li>
                    <li>The inputs to WBS creation are the outputs to the Define Scope and Collect Requirements processes: the requirements document and the project scope statement.</li>
                    <li>As you decompose the work, you find new information that needs to be added to the requirements document and the project scope statement. That information is treated as a change and goes through change control. Once it’s approved, it can be added into the document, and that kicks off the planning cycle again.</li>
                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

