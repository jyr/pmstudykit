                  <h3 class="box-title">Time</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
                    <li>The <strong>critical path</strong> is the path that has the longest duration.</li>
                    <li>You should be able to figure out the number of paths in a <strong>project network diagram</strong>, and the duration of each path.</li>
                    <li>The <strong>float</strong> for an activity is the amount that its duration can slip without causing the project to be delayed. The float for any activity on the critical path is zero.</li>
                    <li>You’ll need to know how to calculate the <strong>early start, late start, early finish, and late finish</strong> for an activity in a network diagram using the forward pass and backward pass. This is the core of critical path analysis.</li>
                    <li>You may see a <strong>PDM</strong> (or <strong>activity-on-node</strong>) diagram with special nodes that have extra boxes in the corners for the ES, EF, LF, and LS.</li>
                    <li>Don’t forget that when two paths intersect, you have to decide which ES or LF value to take for the calculation in the next node. For the <strong>forward pass</strong>, use the larger value; for the <strong>backward pass</strong>, use the smaller one.</li>

                  </ol>
                  <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

