                  <h3 class="box-title">Introduction</h3>
                </div><!-- /.box-header -->
                <div class="box-body">
                  <ol>
              <li><strong>Knowledge, performance, and personal skills</strong> are the three areas that project managers focus on to get better at their jobs.</li>
              <li>A <strong>project charter</strong> is a document that describes a project’s requirements and high-level schedule, assigns a project manager, and authorizes the project.</li>
              <li>A <strong>program</strong> is a collection of projects that should be managed together in order to achieve a specific goal or benefit to the company.</li>
              <li>A <strong>portfolio</strong> is a collection of projects or programs.</li>
              <li>A <strong>project</strong> gathers a team together to do work that’s <strong>temporary</strong>, creates a <strong>unique result</strong>, and is <strong>progressively</strong> elaborated.</li>
              <li>An <strong>operation</strong> (or process) is work that’s done in a way that’s repeatable and ongoing, but is not a project.</li>                  </ol>
              <h6>Excerpt From: Jennifer Greene and Andrew Stellman. “OReilly.Head.First.PMP.3rd.Edition.Dec.2013.epub.” iBooks. </h6>

